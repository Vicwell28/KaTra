'use strict';

/**
 * Devuelve el opuesto aditivo de un número.
 *
 * @param {number} number - El número a invertir.
 * @returns {number} El opuesto del número.
 */
const opposite = number => -number;

// Ejemplos de uso:
console.log(opposite(-35)); // 35
console.log(opposite(35));  // -35
console.log(opposite(0));   // -0 (que es igual a 0 en JavaScript)
